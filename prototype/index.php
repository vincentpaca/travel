<script type="text/javascript">
	$(window).scroll(function() {    
	    // find the li with class 'active' and remove it
	    // $("ul.menu-bottom li.active").removeClass("active");
	    // get the amount the window has scrolled
	    var scroll = $(window).scrollTop();
	    // add the 'active' class to the correct li based on the scroll amount
	    if (scroll >= 565	) {
	        $(".home-selection-nav").addClass('sticky');
	        $(".home-search-wrapper").fadeIn('fast');
	    }
	    else {
	        $(".home-selection-nav").removeClass('sticky');
	        $(".home-search-wrapper").fadeOut('fast');
	    }
	 
	});
	$(".vote-btn").click(function(){
		$(this).addClass('active').siblings().removeClass('active');

	});
	$(document).ready(function(){
		$("#loginBtn").click(function(){
			$("#loginModal").fadeIn('fast');
			$('.modal-overlay').fadeIn('fast');
		});
		$("#signupBtn").click(function(){
			$("#signupModal").fadeIn('fast');
			$('.modal-overlay').fadeIn('fast');
		});
		$("#signupEmailLink").click(function(){
			$("#signupEmail2").show();
			$("#signupEmail").hide();
			$("#signupSocial").hide();
		});
		$("#login1").click(function(){
			$("#signupModal").hide();
			$("#loginModal").show();
			$("#signupSocial").show();
			$("#signupEmail").show();
			$("#signupEmail2").hide();
		});
		$("#signUp1").click(function(){
			$("#loginModal").hide();
			$("#signupModal").show();
		});
		
		$(".home-features-nav a").click(function(){
			$(this).parent().addClass('active').siblings().removeClass('active');
		});

		$('.modal-overlay').click(function(){
			$(this).fadeOut('fast');
			$('.modal').fadeOut('fast');
		});

	});
</script>
